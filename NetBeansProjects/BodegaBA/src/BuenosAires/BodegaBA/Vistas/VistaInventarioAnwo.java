    private void btnMostarInventarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostarInventarioActionPerformed
        ArrayList<AnwoProducto> lista = controller.obtenerInventarioAnwo();
        String col[] = {"ID Producto", "Nombre Producto", "Precio"};
        DefaultTableModel modeloTabla = new DefaultTableModel(col, 0);
        //columnas iniciales con nombres logicos
        for (AnwoProducto prod : lista) {
            Object[] fila = new Object[3];
            fila[0] = prod.getIdProducto();
            fila[1] = prod.getNomProducto();
            fila[2] = prod.getPrecio();
            modeloTabla.addRow(fila);
        }
        this.jtAnwoProductoLista.setModel(modeloTabla);
    }//GEN-LAST:event_btnMostarInventarioActionPerformed

