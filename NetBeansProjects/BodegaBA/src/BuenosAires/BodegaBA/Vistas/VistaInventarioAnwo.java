    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        // verifico si el texto estÃ¡ vacÃ­o
        if(this.tfIdStock.getText() == null){
            JOptionPane.showMessageDialog(rootPane, "Ingrese un id de stock");
        }
        int idStock = 0;
        // intento transformar el texto a integer
        try{
            idStock = Integer.parseInt(this.tfIdStock.getText());
        }
        catch(Exception e){
            // si ocurre un error al transformar, lanzo un mensaje
            JOptionPane.showMessageDialog(rootPane, "El valor debe ser numÃ©rico");
            // termino el mÃ©todo para que no continÃºe haciendo lo de abajo
            return;
        }
        ResultadoSP reserva =controller.reservar(idStock);
        if(reserva != null){
            if(reserva.getCodigo().equals("Error")){
                JOptionPane.showMessageDialog(rootPane, reserva.getDescripcion());
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Producto reservado");
                ArrayList<AnwoStockProducto> lista = controller.obtenerStockAnwo();
                String col[] = {"ID Stock", "ID Producto", "CÃ³digo", "Reservado"};
                DefaultTableModel modeloTabla = new DefaultTableModel(col, 0);
                //columnas iniciales con nombres logicos
                for (AnwoStockProducto prod : lista) {
                    Object[] fila = new Object[4];
                    fila[0] = prod.getIdStock();
                    fila[1] = prod.getIdProducto();
                    fila[2] = prod.getCodigo();
                    fila[3] = prod.getReservadoBa();
                    modeloTabla.addRow(fila);
                }
                this.jtAnwoProductoStock.setModel(modeloTabla);
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Error al reservar");
        }
    }//GEN-LAST:event_btnReservarActionPerformed

